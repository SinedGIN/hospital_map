function search_map_image() {
    const input = document.getElementById("search_input").value.toLowerCase().trim();


    var imageSrc = Object.keys(mapImages);

    for (const key of imageSrc) {
        if (key.toLowerCase().replace(/ /g,'') == input.replace(/ /g,'')) {
            document.getElementById('search_input').style.boxShadow = "0 0 3px 3px 36DD38";
            window.location.replace("./pages/redirect__page/index.html?cabinet=" + key);
            return;
        }
    }
    document.getElementById("search_input").value = "";
    var alert = document.getElementById('search_alert');
    alert.innerHTML = 'Кабинет не найден';

    document.getElementById('search_input').style.boxShadow = "0 0 3px 3px rgb(188, 1, 23)";

}


function EnterSearch(key){

    let block_hints = document.getElementById('hints_search')

    document.getElementById('search_input').value = key;
    block_hints.innerHTML = '';
   

    document.getElementById('search_input').style.borderRadius = '15px';

    block_hints.innerHTML = '';
    block_hints.style.backgroundColor = 'rgba(255, 255, 255, 0)';
    block_hints.style.boxShadow = 'none';
    block_hints.style.border = 'none';
    block_hints.style.borderRadius = 'none'
}

